package valiable;

public class Val5 {
    public static void main(String[] args) {
        //1. 변수 선언, 초기화 각각 따로
        int a;
        a = 1;
        System.out.println(a);

        int b = 2; //2. 변수 선언과 초기화 한번에
        System.out.println(b);

        int c = 3, d = 4; //3. 여러 변수의 선언과 초기화 한번에
        System.out.println(c);
        System.out.println(d);
    }
}
